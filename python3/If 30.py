L = [1,21,80,99,100,101,800,901,999]

for i in L:
    if i%2 == 0:
        s = "четное "
    else:
        s = "нечетное "

    i_str = str(i)
    n = len(i_str)
    if n == 1:
        s += "однозначное "
    elif n == 2:
        s += "двузначное "
    elif n == 3:
        s += "трехзначное "
    
    s += "число"
    print(i," : ",s)